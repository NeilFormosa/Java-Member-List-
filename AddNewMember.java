    public static boolean lettersOnly(String s){
        for (int i = 0; i < s.length(); i++){
            char x = s.charAt(i);
            if(!Character.isLetter(x)){
                return false;
            }
        }
        return true;
    }
    
    public static boolean digitsOnly(String s){
        for (int i = 0; i < s.length(); i++){
            char x = s.charAt(i);
            if(!Character.isDigit(x)){
                return false;
            }
        }
        return true;
    }
    
