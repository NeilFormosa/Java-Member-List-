    private void saveBTNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveBTNMouseClicked
        String name = namefield.getText();
        String surname = surnameField.getText();
        String phone = phoneField.getText();
        String DOB = DOBField.getText();
        String email = emailField.getText();
        String startDate = startDateField.getText();
        String endDate = endDateField.getText();
        String team = teamCB.getSelectedItem().toString();
        String position = positionCB.getSelectedItem().toString();
        String gender = "";
        LocalDate Now = LocalDate.now();
        
        if (maleRB.isSelected()){
            gender = "Male";
        }else{
            gender = "Female";
        }
        
        boolean validName = false;
        boolean validSurname = false;
        boolean validPhone = false;
        boolean validDate = true;
        boolean validDate2 = true;
        boolean validDOB = true;
        
        if (!name.isBlank()){
            validName = lettersOnly(name);
        }
        if (!surname.isBlank()){
            validSurname = lettersOnly(surname);
        }
        if (!phone.isBlank()){
           validPhone = digitsOnly(phone); 
        }else{
            validPhone = true;
        }
        
        DateTimeFormatter format = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate DOB1 = null;
        LocalDate startDate1 = null;
        LocalDate endDate1 = null;
        
        try {
            DOB1 = LocalDate.parse(DOB, format);
        } catch (Exception DateTimeParseException) {
            validDOB = false;
        }
        try {
            startDate1 = LocalDate.parse(startDate, format);
        } catch (Exception DateTimeParseException) {
            validDate = false;
        }
        try {
            endDate1 = LocalDate.parse(endDate, format);
            
            if (endDate1.isBefore(startDate1)){
                validDate2 = false;
            }
        } catch (Exception DateTimeParseException) {
            if (endDate.equals("dd/MM/yyyy")){
                validDate2 = true;
            }else{
                validDate2 = false;
            }
        }
        
       if (validDate){
           if (startDate1.isAfter(Now)){
               validDate = false;
           }
       }
       
       if (validDOB && DOB1.isAfter(Now)){
           validDOB = false;
       }
       
       if(!validName){
           JOptionPane.showMessageDialog(this, "Error in Name");
       }
       if(!validSurname){
           JOptionPane.showMessageDialog(this, "Error in Surname");
       }
       if (!validPhone){
           JOptionPane.showMessageDialog(this, "Error in phone");
       }
       if(!validDOB){
           JOptionPane.showMessageDialog(this, "Error in Date of Birth");
       }
       if(!validDate || !validDate2){
           JOptionPane.showMessageDialog(this, "Error in membership dates");
       }
       
       if(validName && validSurname && validPhone && validDOB && validDate && validDate2){
           int confirm = JOptionPane.showConfirmDialog(this, ("Are you sure you want to save this data:\n")+ ("Name: " + name + "\n") + ("Surname: " + surname + "\n") + ("Date of Birth: " + DOB + "\n") + ("Gender: " + gender + "\n") + ("Phone: " + phone + "\n") + ("Email: " + email + "\n") + ("Team: " + team + "\n") + ("Position: " + position + "\n") + ("Start Date: " + startDate + "\n") + ("End Date: " + endDate + "\n") + ("FilePath: " + pathPhoto + "\n"));
           if (confirm == 0){
               try {
                   File myFile = new File("Members.csv");
                   if (!myFile.exists()){
                       myFile.createNewFile();
                   }

                   FileWriter Writer = new FileWriter(myFile, true);
                   BufferedWriter buffWriter = new BufferedWriter(Writer);
                   buffWriter.write(name + "," + surname + "," + team + "\n");
                   buffWriter.close();
               } catch (IOException ex) {
                   System.out.println("Something went wrong...");
               }
               this.dispose();
           }
       }
    }//GEN-LAST:event_saveBTNMouseClicked

